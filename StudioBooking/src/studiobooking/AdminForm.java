    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed

    try {
    Connection conn = DBConnection.getConnection();
    String sql = "INSERT INTO booking (nama, tanggal, jam_mulai, jam_selesai, durasi, studio, status) VALUES (?, ?, ?, ?, ?, ?, ?)";
    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setString(1, namaField.getText());

    java.util.Date selectedDate = tanggalField.getDate();
    java.sql.Date sqlDate = new java.sql.Date(selectedDate.getTime());
    pst.setDate(2, sqlDate);

    pst.setString(3, jamMulaiField.getText());
    pst.setString(4, jamSelesaiField.getText());
    pst.setString(5, durasiField.getText());
    pst.setString(6, studioField.getText());
    pst.setString(7, "pending");

    pst.executeUpdate();

    JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");

    pst.close();
    conn.close();

    // Ini WAJIB agar JTable langsung refresh datanya
    tampilkanDataBooking();

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
}


    }//GEN-LAST:event_simpanButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
                                         
    try {
        Connection conn = DBConnection.getConnection();
        String sql = "UPDATE booking SET nama=?, tanggal=?, jam_mulai=?, jam_selesai=?, durasi=?, studio=?, status=? WHERE id_booking=?";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, namaField.getText());
        java.util.Date selectedDate = tanggalField.getDate();
        java.sql.Date sqlDate = new java.sql.Date(selectedDate.getTime());
        pst.setDate(2, sqlDate);
        pst.setString(3, jamMulaiField.getText());
        pst.setString(4, jamSelesaiField.getText());
        pst.setString(5, durasiField.getText());
        pst.setString(6, studioField.getText());
        pst.setString(7, (String) statusComboBox.getSelectedItem());
        pst.setInt(8, Integer.parseInt(idBookingField.getText())); // ID disembunyikan tapi tetap dibaca

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
        tampilkanDataBooking(); // refresh table
        pst.close();
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal edit data: " + e.getMessage());
    }


    }//GEN-LAST:event_editButtonActionPerformed

    private void bookingTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bookingTableMouseClicked
                                          
    int row = bookingTable.getSelectedRow();
    if (row != -1) {
        idBookingField.setText(bookingTable.getValueAt(row, 0).toString());
        namaField.setText(bookingTable.getValueAt(row, 1).toString());
        tanggalField.setDate(java.sql.Date.valueOf(bookingTable.getValueAt(row, 2).toString()));
        jamMulaiField.setText(bookingTable.getValueAt(row, 3).toString());
        jamSelesaiField.setText(bookingTable.getValueAt(row, 4).toString());
        durasiField.setText(bookingTable.getValueAt(row, 5).toString());
        studioField.setText(bookingTable.getValueAt(row, 6).toString());
        statusComboBox.setSelectedItem(bookingTable.getValueAt(row, 7).toString());

        // Aktifkan tombol edit dan hapus, matikan simpan
        simpanButton.setEnabled(false);
        
        editButton.setEnabled(true);
        hapusButton.setEnabled(true);
    }


    }//GEN-LAST:event_bookingTableMouseClicked

